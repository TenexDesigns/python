In Python, type casting refers to the conversion of one data type to another data type. 
Python provides various built-in functions to perform type casting. Here are some examples:



1.  int(): This function is used to convert a value to an integer data type.

x = '10'
print(int(x))  # Output: 10


2.  float(): This function is used to convert a value to a floating-point data type.

y = '3.14'
print(float(y))  # Output: 3.14



3.  str(): This function is used to convert a value to a string data type.

z = 123
print(str(z))  # Output: '123'



4. tuple(): This function is used to convert a value to a tuple data type.


b = [1, 2, 3]
print(tuple(b))  # Output: (1, 2, 3)



5.bool(): This function is used to convert a value to a Boolean data type.

c = 0
print(bool(c))  # Output: False














































































































